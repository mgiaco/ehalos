int myExp(int basis, int exponent){
	int i=0;
	int sum=1;

	for(i=0; i < exponent; i++){
		sum =  sum * basis;
	}

	return sum;
}

unsigned char ascii_to_dec(char hex){
	if(hex >= (int)'A' && hex <= (int)'F'){
		return 10 + hex - (int)'A';
	}else{
		return hex - '0';
	}
}

int hex_to_dec(char* buffer, int size){
	int i=0;
	int dec=0;
	for(i=0; i < size; i++){
		dec = dec  + myExp(16, i) * (ascii_to_dec(buffer[size-i-1]));
	}
	return dec;
}

int read_start_code(char** hexbuffer){

	int nextsymbol = **hexbuffer;
	(*hexbuffer)++;
	return nextsymbol;
}

int read_byte_count(char** hexbuffer){
	char hexbytecount[2];
	int bytecount=0;
	hexbytecount[0] = **hexbuffer;
	(*hexbuffer)++;
	hexbytecount[1] = **hexbuffer;
	(*hexbuffer)++;
	bytecount = hex_to_dec(hexbytecount, 2);
	return bytecount;
}

int read_address(char** hexbuffer){
	char hexaddress[4];
	int address = 0;

	hexaddress[0] = **hexbuffer;
	(*hexbuffer)++;
	hexaddress[1] = **hexbuffer;
	(*hexbuffer)++;
	hexaddress[2] = **hexbuffer;
	(*hexbuffer)++;
	hexaddress[3] = **hexbuffer;
	(*hexbuffer)++;

	address = hex_to_dec(hexaddress, 4);

	return address;

}

int read_type(char** hexbuffer){
	char hextype[2];
	int type = -1;
	hextype[0] = **hexbuffer;
	(*hexbuffer)++;
	hextype[1] = **hexbuffer;
	(*hexbuffer)++;

	type = hex_to_dec(hextype,2);

	return type;
}

void read_data(char** hexbuffer, char* data, int count){
	int i=0;
	char hexvalue[2];
	int value = 0;
	for(i=0; i < count; i++){
		hexvalue[0] = **hexbuffer;
		(*hexbuffer)++;
		hexvalue[1] = **hexbuffer;
		(*hexbuffer)++;

		value = hex_to_dec(hexvalue, 2);
		*data = value;
		data++;
	}
}

void clear_buffer(char* buffer, int count){
	int i=0;
	for(i=0; i < count; i++){
		*buffer = 0;
		buffer++;
	}
}

int check_checksum(char** hexbuffer, char* checksumstart, int bytecount){

	int lenbytes = 4+bytecount+1;
	char hexvalue[2];
	int i=0;
	int sum = 0;
	int value = 0;

	for(i=0; i < lenbytes; i++){
		hexvalue[0] = *checksumstart;
		checksumstart++;
		hexvalue[1] = *checksumstart;
		checksumstart++;

		value = hex_to_dec(hexvalue,2);
		sum = sum + value;
	}


	sum = sum % 256;

	(*hexbuffer)= (*hexbuffer) + 2;


	return sum == 0;

}

void copy_data_to_ram(char* data, int baseaddress,  int count){
	int i=0;
	for(i=0; i < count; i++){
		ram[baseaddress+i] = *data;
		data++;
	}
}

